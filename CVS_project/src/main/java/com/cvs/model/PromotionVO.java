package com.cvs.model;

public class PromotionVO {
	
	private int md_no;
	private String pr_type;
	private String pr_subject;
	private String pr_content;
	private String start_pr_date;
	private String end_pr_date;
	public int getMd_no() {
		return md_no;
	}
	public void setMd_no(int md_no) {
		this.md_no = md_no;
	}
	public String getPr_type() {
		return pr_type;
	}
	public void setPr_type(String pr_type) {
		this.pr_type = pr_type;
	}
	public String getPr_subject() {
		return pr_subject;
	}
	public void setPr_subject(String pr_subject) {
		this.pr_subject = pr_subject;
	}
	public String getPr_content() {
		return pr_content;
	}
	public void setPr_content(String pr_content) {
		this.pr_content = pr_content;
	}
	public String getStart_pr_date() {
		return start_pr_date;
	}
	public void setStart_pr_date(String start_pr_date) {
		this.start_pr_date = start_pr_date;
	}
	public String getEnd_pr_date() {
		return end_pr_date;
	}
	public void setEnd_pr_date(String end_pr_date) {
		this.end_pr_date = end_pr_date;
	}


	

}